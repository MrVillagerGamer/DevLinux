{
	vga_color = c;
	vga_default_color = c;
}

void vga_text_clear()
{
	uint32_t i;
	uint32_t j;
	uint8_t* vram = (uint8_t*)0xb8000;
	for(i = 0; i < vga_mode_width; i++)
	{
		for(j = 0; j < vga_mode_height; j++)
		{
			vram[(i+vga_mode_width*j)*2] = ' ';
			vram[(i+vga_mode_width*j)*2+1] = vga_color;
		}
	}
	vga_x = 0;
	vga_y = 0;
}

void vga_text_scroll_down()
{
	uint32_t i;
	uint32_t j;
	
	uint8_t* vram = (uint8_t*)0xb8000;
	
	for(i = 0; i < vga_mode_width; i++)
	{
		for(j = 1; j < vga_mode_height; j++)
		{
			vram[(i+vga_mode_width*(j-1))*2] = vram[(i+vga_mode_width*j)*2];
			vram[(i+vga_mode_width*(j-1))*2+1] = vram[(i+vga_mode_width*j)*2+1];
		}
		vram[(i+vga_mode_width*(vga_mode_height-1))*2] = ' ';
		vram[(i+vga_mode_width*(vga_mode_height-1))*2+1] = vga_color;
	}
}

void vga_text_put_char(uint8_t c)
{

	uint8_t* vram = (uint8_t*)0xb8000;
	switch(c)
	{
	case '\n':
		vga_x = 0;
		vga_y++;
		break;
	default:
		vram[(vga_x+vga_mode_width*vga_y)*2] = c;
		vram[(vga_x+vga_mode_width*vga_y)*2+1] = vga_color;
		vga_x++;
		break;
	}
	if(vga_x >= vga_mode_width)
	{
		vga_x = 0;
		vga_y++;
	}
	if(vga_y >= vga_mode_height)
	{
		vga_text_scroll_down();
		vga_y--;
	}
}

void vga_text_put_char_big(uint8_t c)
{
	uint8_t* vram = (uint8_t*)0xb8000;
	if(c != '\n')
	{
		for(uint32_t x = vga_x; x < vga_x+vga_font_width; x++)
		{
			for(uint32_t y = vga_y; y < vga_y+vga_font_height; y++)
			{
				uint32_t y2 = y / 2;		
				vram[(x+vga_textmode_width*y2)*2] = 220;
				vram[(x+vga_textmode_width*y2)*2+1] = 0;
			}
		}
		for(uint32_t x = vga_x; x < vga_x+vga_font_width; x++)
		{
			for(uint32_t y = vga_y; y < vga_y+vga_font_height; y++)
			{
				
				uint32_t y2 = y / 2;	
				if(vga_8x8_font[c*vga_font_height+(y-vga_y)] & (1 << (vga_font_width-(x-vga_x))))
				{
					if((y % 2) == 0)
					{
						vram[(x+vga_textmode_width*y2)*2] = 220;
						vram[(x+vga_textmode_width*y2)*2+1] = (vram[(x+vga_textmode_width*y2)*2+1] & 0x0F) | 0x70;
					}
					if((y % 2) == 1)
					{
						vram[(x+vga_textmode_width*y2)*2] = 220;
						vram[(x+vga_textmode_width*y2)*2+1] = (vram[(x+vga_textmode_width*y2)*2+1] & 0xF0) | 0x07;
					}
				}
			}
		}
		vga_x += vga_font_width;
		if(vga_x >= vga_textmode_width)
		{
			vga_x = 0;
			vga_y += vga_font_height;
		}
		while(vga_y >= vga_textmode_height)
		{
			vga_text_scroll_down();
			vga_y--;
		}
	}
	else
	{
		vga_x = 0;
		vga_y += vga_font_height;
		while(vga_y >= vga_textmode_height)
		{
			vga_text_scroll_down();
			vga_y--;
		}
	}
}

void vga_text_put_string(uint8_t* str)
{

	uint32_t i = 0;
	while(str[i])
	{
		vga_text_put_char(str[i]);
		i++;
	}

}

void vga_text_put_string_big(uint8_t* str)
{
	uint32_t i = 0;
	while(str[i])
	{
		vga_text_put_char_big(str[i]);
		i++;
	}
}

